package fr.diginamic.operations;

/**
 * Classe de calcul d'addition et de soustraction des entiers
 * @author Sheerin
 */
class Calcul {

	/**
	 * Addition de deux entiers
	 * @param a entier naturel
	 * @param b entier naturel
	 * @return un entier naturel
	 */
	public int additionnerEntier(int a, int b){


		return a+b;
	}

	/**
	 * Soustraction de deux entiers
	 * @param a entier naturel
	 * @param b entier naturel
	 * @return un entier naturel
	 */
	public int soustraireEntier(int a, int b){

		return a-b;
	}
}
